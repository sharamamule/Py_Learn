"""
== --> Value Equality
!= --> Not equal to
< --> Less than
> --> Greater than
<= --> Less than or equal to
>= --> Greater than or equal to
"""

bool_one=10 ==10
print(bool_one)
bool_two=10 ==11
print(bool_two)

bool_one=10 !=10
print(bool_one)

bool_one=10 <10
print(bool_one)

bool_one=10 >10
print(bool_one)
print("*"*20)
bool_one=9 <=10
print(bool_one)
print("*"*20)
bool_one=10 >=11-2
print(bool_one)