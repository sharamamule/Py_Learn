## Comparision Operators
"""
== --> Value Equality
!= --> Not equal to
<  --> Greater than
<= --> less than or equal to
>= --> Greater than or equal to
"""

bool_one = 10 ==10
print(bool_one)
not_equal = 10 !=10
print(not_equal)
less_than = 10 <= 9
print(less_than)
great_than_equal =10 >= 10-2
print(great_than_equal)
